    private void fillTablePhong() {
        DefaultTableModel model = (DefaultTableModel) tblPhong.getModel();
        model.setRowCount(0);
        try {
            List<Phong> list = ps.selectAll();
            for (Phong dv : list) {
                Object[] row = {
                    dv.getTang(),
                    dv.getMaP(),
                    dv.getMaLP(),
                    dv.getTrangThai()};
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi fillTablePhong");
            e.printStackTrace();
        }
    }

    void showDTDV(Phong p) {
        txtTang.setText(p.getTang() + "");
        txtSoPhong.setText(p.getMaP());
        cboLoaiP.getModel().setSelectedItem(loaiPhongDAO.selectByID(p.getMaLP()));
        cboTrangthai.getModel().setSelectedItem(trangThaiDAO.selectByID(p.getTrangThai() + ""));
    }

    private void edit() {
        try {
            String MaP = (String) tblPhong.getValueAt(index, 1);
            Phong p = (Phong) ps.selectByID(MaP);
            if (p != null) {
                showDTDV(p);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void fillTableTop5Phong() {
        DefaultTableModel model = (DefaultTableModel) tblTop5Phong.getModel();
        model.setRowCount(0);
        try {
            List<Phong> list = ps.selectTop5();
            for (Phong dv : list) {
                Object[] row = {
                    dv.getTang(),
                    dv.getMaP(),
                    dv.getMaLP(),
                    dv.getSosd(),
                    dv.getTrangThai(),};
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi fillTableTop5Phong");
            e.printStackTrace();
        }
    }

    Phong getForm() {
        Phong p = new Phong();
        LoaiPhong loaiphong =  (LoaiPhong) cboLoaiP.getSelectedItem();
        p.setMaLP(loaiphong.getMaLP());
        
        TrangThaiPhong trangthai = (TrangThaiPhong) cboTrangthai.getSelectedItem();
        p.setTrangThai(trangthai.getIdTTPhong());
         
        p.setMaP(txtSoPhong.getText());       
        p.setTang(Integer.parseInt(txtTang.getText()));
       
        return p;
    }

    private void add() {
        Phong model = getForm();
        try {
            ps.them(model);
            clear();
            fillTablePhong();
            JOptionPane.showMessageDialog(this, "Thêm phòng thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
            e.printStackTrace();
        }
    }

    private void clear() {
        cboLoaiP.setSelectedIndex(0);
        txtSoPhong.setText("");
        txtTang.setText("");
        cboTrangthai.setSelectedIndex(0);
    }

    private void ud() {
        Phong model = getForm();
        try {
            ps.sua(model);
            fillTablePhong();
            fillTableTop5Phong();
            JOptionPane.showMessageDialog(this, "Sửa phòng thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sửa thất bại");
            e.printStackTrace();
        }
    }

    private void fillComboLoaiPhong() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cboLoaiP.getModel();
        model.removeAllElements();
        List<LoaiPhong> list = loaiPhongDAO.selectAll();
        for (LoaiPhong lp : list) {
            model.addElement(lp);
        }
    }

    private void themLoaiPhong() {
        LoaiPhong model = getFormLoaiPhong();
        try {
            loaiPhongDAO.them(model);
            txtGiaTheoGio1.setText("");
            txtTenLoaiPhong1.setText("");
            fillComboLoaiPhong();
            JOptionPane.showMessageDialog(this, "Thêm loại phòng thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
            e.printStackTrace();
        }
    }


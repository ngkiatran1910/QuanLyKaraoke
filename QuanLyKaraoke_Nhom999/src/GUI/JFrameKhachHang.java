    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
        model.setRowCount(0);
        try {
            List<KhachHang> list = Service.selectAll();
            for (KhachHang kh : list) {
                model.addRow(new Object[]{
                    kh.getMaKH(), kh.getTenKH(), kh.getSDT()
                });
            }
        } catch (Exception e) {

        }
    }

    KhachHang getForm() {
        KhachHang kh = new KhachHang();
        kh.setMaKH(txtMaKH.getText());
        kh.setTenKH(txtTenKhachHang.getText());
        kh.setSDT(txtSDT.getText());
        return kh;
    }

    private void setForm(KhachHang kh) {
        txtMaKH.setText(kh.getMaKH());
        txtTenKhachHang.setText(kh.getTenKH());
        txtSDT.setText(kh.getSDT());
    }

    private void add() {
        KhachHang kh = getForm();
        try {
            Service.them(kh);
            fillTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "Thêm mới thành công!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Thêm mới thất bại!");
            e.printStackTrace();
        }
    }

    private void update() {
        KhachHang kh = getForm();
        try {
            Service.sua(kh);
            fillTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "Sửa mới thành công!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Sửa mới thất bại!");
            System.out.println(e);
        }
    }

    private void clearForm() {
        txtMaKH.setText("");
        txtTenKhachHang.setText("");
        txtSDT.setText("");
    }

    private void edit() {

        String makh = (String) tblKhachHang.getValueAt(indext, 0);
        KhachHang kh = (KhachHang) Service.selectByID(makh);
        setForm(kh);

    }
